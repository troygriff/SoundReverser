//ListStackNode stores the data of a node, and the next node it is connected to
public class ListStackNode {   
    public double data;
    public ListStackNode next;
    
    
    //constructs a ListStackNode with the given parameters, double data and ListStackNode next
    public ListStackNode(double data, ListStackNode next) {
            this.data = data;
            this.next = next;
    } 	
}